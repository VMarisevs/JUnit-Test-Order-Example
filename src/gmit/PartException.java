package gmit;

public class PartException extends Throwable {
	private static final long serialVersionUID = 1L;

	public PartException() {
		super();
	}

	public PartException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public PartException(String arg0) {
		super(arg0);
	}

	public PartException(Throwable arg0) {
		super(arg0);
	}

}
